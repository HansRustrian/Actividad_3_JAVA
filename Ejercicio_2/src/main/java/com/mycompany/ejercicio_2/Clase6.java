
package com.mycompany.ejercicio_2;


// ESTO ES UN OBJETO, PUEDE HABER N CANTIDAD DE OBJETOS
// OBJETO == CLASE
public class Clase6 {
    
    // ATRIBUTOS DE UN EQUIPO DE FUTBOL
    private String nombre;
    private int jugadores;
    private String nombre_entrenador;
    private String estrella;
    private String color_uniforme;
    private int copas;

    
    // get y set de variables declaradas antes
    // ESTOS SON METODOS 
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getJugadores() {
        return jugadores;
    }

    public void setJugadores(int jugadores) {
        this.jugadores = jugadores;
    }

    public String getNombre_entrenador() {
        return nombre_entrenador;
    }

    public void setNombre_entrenador(String nombre_entrenador) {
        this.nombre_entrenador = nombre_entrenador;
    }

    public String getEstrella() {
        return estrella;
    }

    public void setEstrella(String estrella) {
        this.estrella = estrella;
    }

    public String getColor_uniforme() {
        return color_uniforme;
    }

    public void setColor_uniforme(String color_uniforme) {
        this.color_uniforme = color_uniforme;
    }

    public int getCopas() {
        return copas;
    }

    public void setCopas(int copas) {
        this.copas = copas;
    }
    
    
    
}
